class RepeatFilter(logging.Filter):
    """–§–∏–ª—å—Ç—Ä –¥–ª—è –ø—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–µ–Ω–∏—è –ø–æ–≤—Ç–æ—Ä—è—é—â–∏—Ö—Å—è —Å–æ–æ–±—â–µ–Ω–∏–π –æ–± –æ—à–∏–±–∫–∞—Ö"""
    def __init__(self):
        self.seen = set()

    def filter(self, record):
        if record.message in self.seen:
            return False  # –ü—Ä–æ–ø—É—Å—Ç–∏—Ç—å –ø–æ–≤—Ç–æ—Ä—è—é—â–µ–µ—Å—è —Å–æ–æ–±—â–µ–Ω–∏–µ
        self.seen.add(record.message)
        return True

repeat_filter = RepeatFilter()
console_handler.addFilter(repeat_filter)

